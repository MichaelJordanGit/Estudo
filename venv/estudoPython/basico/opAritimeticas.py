n1 = int(input('Valor '))
n2 = int(input('Outro valor '))
soma = n1 + n2
sub = n1 - n2
div = n1 / n2
di = n1 // n2
mul = n1 * n2
pot = n1 ** n2

print('a soma é {0}, subtração é {1} e a divisão é {2}'.format(soma, sub, div))
print('A divisão inteira é {0}, multiplicação é {1} e a potencia é {2}'.format(di, mul, pot))
